<script>
    export default {
        name: 'item-positioning',
        props: ['item', 'template'],
        methods: {
            centerHorizontal() {
                this.item.x = (this.template.getElementAreaWidth() - this.item.width) / 2;
            }
        }
    }
</script>


<template>
    <div class="tool-box__section">
        <div class="tool-box__row">
            <div class="tool-box__label">
                Horiz. centered
            </div>
            <div class="tool-box__content">
                <div @click="centerHorizontal()" class="template__tool">
                    <i class="fas fa-arrows-alt-h"></i>
                </div>
            </div>
        </div>
    </div>
</template>


